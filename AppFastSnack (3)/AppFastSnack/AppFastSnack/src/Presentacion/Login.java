    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    // En el botón de Iniciar Sesión:
        String usuario = txtUsuario.getText().trim();
        String contrasena = new String(txtContra.getPassword());

        if (usuario.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese usuario y contraseña");
            return;
        }

        try {
            DAO.UsuarioDAO dao = DAO.UsuarioDAO.getInstance();

            System.out.println("🔐 Intentando login para: " + usuario);

            if (dao.validarUsuario(usuario, contrasena)) {
                JOptionPane.showMessageDialog(this, "✅ Bienvenido: " + usuario);

                // Ir al menú principal (Producto)
                Presentacion.Producto producto = new Presentacion.Producto();
                producto.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, """
                                                    \u274c Usuario o contrase\u00f1a incorrectos
                                                    Si no tienes cuenta, reg\u00edstrate primero""", 
                    "Error de login", 
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (HeadlessException | SQLException | DAOException e) {
            System.err.println("💥 Excepción en login: " + e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "Error al validar: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

