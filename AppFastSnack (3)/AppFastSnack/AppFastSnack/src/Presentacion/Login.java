    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here: 
    String correo = JOptionPane.showInputDialog(this,
        "Ingresa tu correo registrado:",
        "Recuperar contraseña",
        JOptionPane.QUESTION_MESSAGE);

    if (correo == null || correo.trim().isEmpty()) return;

    try {
        DAO.UsuarioDAO dao = DAO.UsuarioDAO.getInstance();
        String contrasena = dao.obtenerContrasenaPorCorreo(correo.trim());

        if (contrasena == null) {
            JOptionPane.showMessageDialog(this,
                "No se encontró ninguna cuenta con ese correo.",
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Generar código de 6 dígitos
        String codigo = String.valueOf((int)(Math.random() * 900000) + 100000);

        // Enviar correo
        enviarCorreo(correo.trim(), codigo);

        // Pedir el código
        String codigoIngresado = JOptionPane.showInputDialog(this,
            "Te enviamos un código a " + correo + "\nIngresa el código:",
            "Verificar código",
            JOptionPane.QUESTION_MESSAGE);

        if (codigo.equals(codigoIngresado)) {
            String nuevaContrasena = JOptionPane.showInputDialog(this,
                "Ingresa tu nueva contraseña:",
                "Nueva contraseña",
                JOptionPane.QUESTION_MESSAGE);
            if (nuevaContrasena != null && nuevaContrasena.length() >= 6) {
                dao.actualizarContrasena(correo.trim(), nuevaContrasena);
                JOptionPane.showMessageDialog(this, "✅ Contraseña actualizada exitosamente.");
            } else {
                JOptionPane.showMessageDialog(this, "La contraseña debe tener al menos 6 caracteres.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "❌ Código incorrecto.");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

private void enviarCorreo(String destinatario, String codigo) throws Exception {
 String remitente = "fastsnackrecuperacion@gmail.com";
String clave = "katqrjooaiktoyon";

    java.util.Properties props = new java.util.Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587");
props.put("mail.smtp.ssl.protocols", "TLSv1.2");
    javax.mail.Session session = javax.mail.Session.getInstance(props,
        new javax.mail.Authenticator() {
            @Override
            protected javax.mail.PasswordAuthentication getPasswordAuthentication() {
                return new javax.mail.PasswordAuthentication(remitente, clave);
            }
        });

    javax.mail.Message mensaje = new javax.mail.internet.MimeMessage(session);
    mensaje.setFrom(new javax.mail.internet.InternetAddress(remitente));
    mensaje.setRecipients(javax.mail.Message.RecipientType.TO,
        javax.mail.internet.InternetAddress.parse(destinatario));
    mensaje.setSubject("🔐 Recuperación de contraseña - FastSnack");
    mensaje.setText("Hola,\n\nTu código de recuperación es: " + codigo +
        "\n\nEste código expira en 5 minutos.\n\nFastSnack 🍔");

    javax.mail.Transport.send(mensaje);

    }//GEN-LAST:event_jButton2ActionPerformed

